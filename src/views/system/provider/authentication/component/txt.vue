      <p>11.6服务商同意遵守康药多服务条款及各项规则，以及康药多制订并不时更新的相关服务规范和管理制度。服务规范和管理制度会不断根据业务发展需要和康药多商家需求进行补充完善，康药多有权以书面、邮件等形式更新通知到服务商。</p>
