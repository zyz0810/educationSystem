      <p>6.2服务商应始终保证保证金余额不低于其所对应的保证金档位金额要求，如发生违规行为扣除保证金导致当前保证金余额低于前述金额时，服务商应在保证金发生扣除之日起<span class="bold">七天内</span>足额补缴，否则视为服务商不再符合合作条件，康药多有权在补缴期间暂停或终止服务商的服务资格。</p>
